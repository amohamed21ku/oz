
class Customer {
  final String cid;
  final String name;
  final String company;
  final String initial;
  final Map<String, dynamic> items;
  final Map<String, dynamic> goods;



  Customer({
    required this.cid,
    required this.name,
    required this.company,
    required this.initial,
    required this.items,
    required this.goods,
  });
}
